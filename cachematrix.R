## Generates a matrix object that will cache the calculation
## of the inverse calculation to speed computation time. The
## cached value will only be calculated when called if the 
## matrix object has been previously modified.

## Initialize the matrix

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)  
}


## Returns the inverse of the input matrix, performing
## the calculation only if needed or the matrix has been
## modified. 
## Assumes that the matrix is always invertable.

cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}
